export function translate(language: Language, key: TranslationKey): string {
  return dictionaries[language]?.[key] || english[key];
}

export function preferredLanguage(): Language {
  const saved = window.localStorage.getItem("flyto-warroom-ce-language");
  if (languages.some(([code]) => code === saved)) return saved as Language;
  const browser = window.navigator.language;
  if (languages.some(([code]) => code === browser)) return browser as Language;
  const base = languages.find(([code]) => code.split("-")[0] === browser.split("-")[0]);
  return base?.[0] || "en";
}
