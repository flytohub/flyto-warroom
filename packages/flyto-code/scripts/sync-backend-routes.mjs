#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const engineRoot = path.resolve(root, process.env.FLYTO_ENGINE_PATH || '../flyto-engine')
const apiDir = path.join(engineRoot, 'api')
const outPath = path.join(root, 'src-next', 'lib', 'engine', '__generated__', 'backend-routes.txt')
const checkOnly = process.argv.includes('--check')
const requireEngine = process.argv.includes('--require-engine')

if (!fs.existsSync(apiDir)) {
  const msg = `sync-backend-routes: backend api dir not found at ${apiDir}`
  if (checkOnly && !requireEngine) {
    console.log(`${msg}; skipping freshness check`)
    process.exit(0)
  }
  console.error(msg)
  process.exit(1)
}

const sources = fs
  .readdirSync(apiDir)
  .filter((name) => name.endsWith('.go') && !name.endsWith('_test.go'))
  .map((name) => path.join(apiDir, name))
const routes = new Set()
const routeRe = /mux\.HandleFunc\(\s*["'`]([A-Z]+)\s+([^"'`]+)["'`]/g
for (const sourcePath of sources) {
  const src = fs.readFileSync(sourcePath, 'utf8')
  let match
  while ((match = routeRe.exec(src)) !== null) {
    routes.add(`${match[1]} ${match[2]}`)
  }
}

const lines = [...routes].sort()
const content = [
  '# Generated by `npm run sync:backend-routes`; do not edit by hand.',
  '# Source: ../flyto-engine/api/*.go',
  '',
  ...lines,
].join('\n') + '\n'

if (checkOnly) {
  const current = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf8') : ''
  if (current !== content) {
    console.error('backend route snapshot is stale; run `npm run sync:backend-routes`')
    process.exit(1)
  }
  console.log(`backend route snapshot fresh: ${lines.length} routes`)
  process.exit(0)
}

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, content)
console.log(`backend route snapshot written: ${lines.length} routes`)
