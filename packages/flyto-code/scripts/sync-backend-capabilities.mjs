#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const outPath = path.join(root, 'src-next', 'lib', 'engine', '__generated__', 'backend-capabilities.yaml')
const capabilitiesRelativePath = 'internal/permission/capabilities.yaml'
const checkOnly = process.argv.includes('--check')
const requireEngine = process.argv.includes('--require-engine')

function engineRootCandidates() {
  return [...new Set([
    process.env.FLYTO_ENGINE_ROOT,
    process.env.FLYTO_ENGINE_DIR,
    process.env.FLYTO_ENGINE_PATH,
    path.resolve(root, '..', 'flyto-engine'),
    path.resolve(root, 'flyto-engine'),
  ].filter(Boolean))]
}

function resolveCapabilitiesPath() {
  const attempted = []
  for (const candidate of engineRootCandidates()) {
    const capabilitiesPath = path.join(path.resolve(candidate), capabilitiesRelativePath)
    attempted.push(capabilitiesPath)
    if (fs.existsSync(capabilitiesPath)) return { capabilitiesPath, attempted }
  }
  return { capabilitiesPath: null, attempted }
}

function generatedContent(raw) {
  return [
    '# Generated by `npm run sync:backend-capabilities`; do not edit by hand.',
    '# Source: ../flyto-engine/internal/permission/capabilities.yaml',
    '',
    raw.trimEnd(),
  ].join('\n') + '\n'
}

const { capabilitiesPath, attempted } = resolveCapabilitiesPath()
if (!capabilitiesPath) {
  const msg = `sync-backend-capabilities: backend capabilities not found; tried ${attempted.join(', ')}`
  if (checkOnly && !requireEngine) {
    console.log(`${msg}; skipping freshness check`)
    process.exit(0)
  }
  console.error(msg)
  process.exit(1)
}

const content = generatedContent(fs.readFileSync(capabilitiesPath, 'utf8'))

if (checkOnly) {
  const current = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf8') : ''
  if (current !== content) {
    console.error('backend capability snapshot is stale; run `npm run sync:backend-capabilities`')
    process.exit(1)
  }
  console.log(`backend capability snapshot fresh: ${path.relative(root, capabilitiesPath)}`)
  process.exit(0)
}

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, content)
console.log(`backend capability snapshot written: ${path.relative(root, outPath)}`)
